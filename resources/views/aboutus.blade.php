    <h1>ITTS Coffee</h1>
    <h4> ITTS Coffee adalah tempat nongkrong yang asik dengan menyajikan minuman yang berkualitas
        tinggi dengan cita rasa yang unik dan menyenangkan. Pada kopi-nya sendiri kami
        menggunakan biji kopi pilihan yang diperoleh dari berbagai daerah penghasil kopi terbaik di dunia.
        Dengan kombinasi antara keahlian kopi yang mendalam, teknologi modern, dan dedikasi terhadap kepuasan pelanggan, kami berusaha untuk memberikan pengalaman kopi yang luar biasa kepada pelanggan kami.
    <br>
    <br>
        Produk dan Layanan :
    <br>
        1. Kopi Segar: ITTS Coffee menawarkan berbagai macam jenis kopi segar yang dipanggang secara teratur untuk menjaga kualitasnya. Kami menyediakan biji kopi single origin dan campuran dengan berbagai tingkat kepekatan dan karakteristik rasa yang berbeda.
    <br>
    <br>
        2. Minuman Khas: Selain kopi hitam, ITTS Coffee juga menyajikan beragam minuman khas seperti Cappuccino, Latte, Espresso, Moccacino, dan banyak lagi. Kami menggunakan teknik penyajian yang terampil dan bahan-bahan berkualitas tinggi untuk menciptakan minuman kopi yang lezat.
    <br>
    <br>
        3. Camilan dan Makanan Ringan: Kami juga menawarkan camilan makanan ringan yang cocok untuk menemani secangkir kopi. Mulai dari Roti Panggang, Kue, Croissant, hingga Sandwich, kami menyajikan pilihan yang beragam untuk memenuhi selera pelanggan.
    <br>
    <br>
        Lokasi :
    <br>
        ITTS Coffee terletak pada Jalan Ketintang Nomer 156, Kecamatan Gayungan, Kota Surabaya 60231 Wonokromo Kota Surabaya, Jawa Timur 60231.
    <br>
    <br>
        Misi :
    <br>
        Misi ITTS Coffee adalah untuk menyajikan kopi terbaik dengan kualitas terbaik kepada pelanggan kami. Kami berkomitmen untuk memahami Prefensi individu setiap pelanggan dan menyediakan pengalaman kopi yang personal dan memuaskan. Kami juga berupaya untuk menjalin hubungan yang berkelanjutan dengan para petani kopi diseluruh dunia, dengan memastikan bahwa biji kopi yang kami gunakan diproduksi secara bertanggung jawab dan adil.
    <br>
    <br>
        Visi :
    <br>
        Visi ITTS Coffee adalah menjadi destinasi kopi pilihan yang diakui secara internasional, di mana orang dapat menikmati kopi berkualiatas tinggi dalam lingkungan yang menyenangkan. Kami ingin memberikan inspirasi dan kepuasan kepada setiap pelanggan kami melalui rasa kopi yang luar biasa dan pelayanan yang luar biasa.
